package com.bogdan.aeroreserve.enums;

/**
 * Перечисление классов мест в самолете
 * Определяет категории комфортности мест
 *
 * @author Bogdan
 * @version 1.0
 */
public enum SeatClass {
    /**
     * Эконом класс - базовый класс обслуживания
     */
    ECONOMY,

    /**
     * Бизнес класс - повышенный комфорт и услуги
     */
    BUSINESS,

    /**
     * Первый класс - максимальный комфорт и премиум услуги
     */
    FIRST
}