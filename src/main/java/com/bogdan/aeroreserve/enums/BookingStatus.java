package com.bogdan.aeroreserve.enums;

/**
 * Перечисление статусов бронирования
 * Определяет возможные состояния бронирования в системе
 *
 * @author Bogdan
 * @version 1.0
 */
public enum BookingStatus {
    /**
     * Ожидание оплаты - бронирование создано, но оплата еще не произведена
     */
    PENDING_PAYMENT,

    /**
     * Подтверждено - бронирование оплачено и активно
     */
    CONFIRMED,

    /**
     * Отменено - бронирование отменено пользователем или системой
     */
    CANCELLED,

    /**
     * Возвращено - средства за бронирование возвращены
     */
    REFUNDED,

    /**
     * Завершено - рейс завершен, бронирование закрыто
     */
    COMPLETED;

    /**
     * Проверяет, является ли статус подтвержденным
     *
     * @return true если статус CONFIRMED, иначе false
     */
    public boolean isConfirmed() {
        return this == CONFIRMED;
    }
}